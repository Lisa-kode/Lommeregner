public class Lommeregner {

        //ADDERING
        public int addering(int a, int b){
            int resultat = a + b; //lige her defineres metoden! som er plus!
            return resultat;
        }

        //SUBSTRAKTION
        public int substraction(int a, int b){
            int resultat = a - b;
            return resultat;
        }

        //MULTIPLIKATION
        public int multiplikation(int a, int b){
            int resultat = a * b;
            return resultat;
        }

        //DELE
        public double dele(double a, double b){
            double resultat = a / b;
            return resultat;
        }

        //SECRET
        public double secret(double a, double b){
            double resultat = (a * b) / (b + 2.0);
            return resultat;
        }


}
